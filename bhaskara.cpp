#include <iostream>
#include <math.h>
using namespace std;
//Resolução do teorema de Bhaskara!


int main(){
	
	//variaveis para  delta e as raizes
	float delta, x1, x2;
	
	//variaveis para os coeficientes
	float a, b, c;
	
	cout << "---Resolução do teorema de Bhaskara---" << endl;
	
	cout << "Digite o valor do coeficiente a: " << endl;
	cin >> a;
	
	cout << "Digite o valor do coeficiente b: " << endl;
	cin >> b;
	
	cout << "Digite o valor do coeficiente c: " << endl;
	cin >> c;
	
	cout << "------" << endl;
	cout << a << "x²+" << b << "x+" << c << endl;
	cout << "------" << endl;
	
	//calcula o valor de delta
	delta = b*b - 4*a*c;
	
	//na primeira raiz tempos o sinal de +
	x1 = (-b + sqrt(delta) )/2*a;
	
	//na segunda raiz temos o sinal de -
	x2 = (-b - sqrt(delta) )/2*a;
	
	cout << "a 1ª raiz é " << x1 << endl;
	cout << "a 2ª raiz é " << x2 << endl;
	
	
	return 0;
}
